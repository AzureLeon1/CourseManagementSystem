  methods: {
    cleanCoursetable() {
      for (let i = 1; i <= 7; i++) {
        for (let j = 1; j <= 12; j++) {
          this.setCourse(i, j, 1, " ", " ");
        }
      }
    },
    setCourse(day, sec, length, name, intro) {
      let secs = document
        .getElementsByClassName("day")
        [day - 1].getElementsByClassName("course");

      secs[sec - 1].getElementsByClassName("name")[0].innerHTML = name;
      secs[sec - 1].getElementsByClassName("intro")[0].innerHTML = intro;
      secs[sec - 1].style.display = "flex";
      secs[sec - 1].style.flex = length;
      if (name != " ") {
        secs[sec - 1].style.border = "#888 1px solid";
      } else {
        secs[sec - 1].style.border = "1px solid #eeeeee90";
      }

      for (let i = 0; i < length - 1; i++) {
        secs[sec + i].style.display = "none";
      }
